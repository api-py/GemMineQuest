/// Real Welsh place names from the Welsh Language Commissioner's standardised list.
/// Coal mine settlements ([PWLL GLO]) are listed first, then general Welsh places.
enum WelshPlaceNames {

    struct Place {
        let name: String
        let coordinate: CLLocationCoordinate2D
    }

    static func name(for level: Int) -> String {
        let index = (level - 1) % allNames.count
        return allNames[index]
    }

    static func place(for level: Int) -> Place {
        let name = self.name(for: level)
        if let coord = knownCoordinates[name] {
            return Place(name: name, coordinate: CLLocationCoordinate2D(latitude: coord.0, longitude: coord.1))
        }
        // For coal mine towns (first 229), use South Wales valleys default region
        let index = (level - 1) % allNames.count
        let seed = Double(index)
        let isCoal = index < 229
        let baseLat = isCoal ? 51.65 : 52.2
        let baseLon = isCoal ? -3.35 : -3.6
        // Deterministic spread based on index
        let latOffset = sin(seed * 0.7) * 0.15
        let lonOffset = cos(seed * 0.5) * 0.25
        return Place(name: name, coordinate: CLLocationCoordinate2D(
            latitude: baseLat + latOffset, longitude: baseLon + lonOffset))
    }

